            <h3>Reseña Histórica</h3>
           <p>
               Han pasado treinta (34) años desde la conformación de la histórica Sociedad de Amigos del Tecnológico, la cual estuvo constituida por un grupo de Portugueseños (periodistas, docentes, empresarios, entre otros) con visión futurista y emprendedora, quienes se dieron a la noble tarea de gestionar las bases fundamentales para la creación del Instituto Universitario de Tecnología del Estado Portuguesa (IUTEP). Fue así como, después de muchas diligencias, viajes a la ciudad de Caracas y reuniones de coordinación, su labranza en tierra fértil dio finalmente frutos a partir del 2 de mayo de 1978, cuando por resolución Ministerial N° 126, se decide designar la Comisión para el estudio de la creación del IUTEP, conformada por el  Ing. Honorio Ariza (+) como Coordinador, Argenis Vivas (+), Francisco Barrios (+), Rafael López, Manuel Escalona (+), Aníbal Santeliz y Galeana de Martínez. Por su parte, correspondió al Ing. Víctor Perdomo asesorar la Comisión y coordinar la elaboración de todos los programas de asignaturas de las especialidades aprobadas (Tecnología  Administrativa  y Tecnología Agropecuaria). Nace el Instituto Universitario de Tecnología del Estado Portuguesa (IUTEP), mediante el Decreto de Creación N° 2859, de fecha 19 de septiembre de 1978, a tenor del artículo 10 de la Ley de Universidades.<br/>
                Hay que distinguir también los méritos del Perito Agropecuario César Gómez Alcántara, quien donó 45 Has. de terreno para la edificación de la sede principal, cuyos tres primeros módulos se inauguraron en 1984, luego de haber funcionado en las sedes provisionales del  Edificio Biomédico (lapso 79-81), Edificio Aldebarán (Lapso 1981-83), Colegio San Vicente de Paúl (1980-84) y la Quinta Virginia (1981-84).<br/>
              Para el año 1996, mediante Oficio número 5.084, se crea la Ampliación del IUTEP en la ciudad de Guanare y el 25 de marzo de 1999, el  Programa  IUTEP-Esteller-Santa Rosalía en  la población de la Colonia Agrícola de Turén.<br/>
              En el mes de febrero del año 2012, y luego de una serie de gestiones efectuadas por los miembros de la Comisión de Modernización y Transformación de esta casa de estudios, ante instancia del Misterio de Educación Superior actual Ministerio del Poder Popular para la Educación Universitaria, el comandante Presidente Hugo Chávez Frías anuncia la creación de de la Universidad Politécnica Territorial del estado Portuguesa “Juan de Jesús Montilla”, Decreto Presidencial Nº 8.802, de fecha 14 de febrero de 2012, publicado en la Gaceta Oficial nº 39.902, de fecha 13 de abril de 2012. A partir de entonces, el Instituto Universitario de Tecnología del Estado Portuguesa, emblemático IUTEP, se convierte en Universidad Politécnica Territorial del estado Portuguesa “Juan de Jesús Montilla”.
           </p> 
